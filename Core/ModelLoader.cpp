Model *ModelLoader::loadCube() {
	//Load texture
	TextureHandle tex = ResLoader::loadTexture("uvtemplate.dds");
	if (tex == nullptr) {
		return nullptr;
	}

	int vertexCount = (sizeof(cube) / sizeof(float)) / 3;
	VertexArray *vertices = GraphicsLoader::generateVertices(vertexCount, (float*)cube, 3, (float*)cubeUV, 2);
	if (vertices == nullptr) {
		releaseHandle(tex);
		return nullptr;
	}

    return new Model(vertices, new Texture(tex));
}
